    private void sSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sSubmitMouseClicked
        // TODO add your handling code here:
         String name = sName.getText();
         String about = sAbout.getText();
         String dob = sDOB.getDate().toString();
//         System.out.println(dob);     dob check korar jonno
         
         String email = sEmail.getText();
         String cell = sCell.getText();
         String subject = sSubject.getSelectedItem().toString(); // Subject String hoyar jonno toString method use korte hobe
         
//         System.out.println(subject); subjuct check

         
         String gender = " ";  // gender box create 
         if(sMale.isSelected()){         
             gender ="Male";
         }
         else if(sFemale.isSelected()){
              gender = "Female";
         }
          else if(sOther.isSelected()){
              gender = "Other";
         }
          else{         
              gender = "Nothing Selected";
          }
 //      System.out.println(gender);  gender check korar jonno
         
         
         
         List<String> hobby = new ArrayList<>();  // List create 
            if(sSport.isSelected()){
                hobby.add("Sport");
            }
            if (sReading.isSelected()){
                hobby.add("Reading");
            }
            if(sTraveling.isSelected()){
                hobby.add("Traveling");
            }
            if (sWriting.isSelected()){
                hobby.add("Writing");
            } 

//            System.out.println(hobby);  hobby check korar jonno
         
        
        try {
            PrintWriter write = new PrintWriter("Student_Info.txt");
            write.print("   Name          : "+name+"\n");
            write.print("   Date of Birth : "+dob+"\n");
            write.print("   E-mail        : "+email+"\n");
            write.print("   Phone no      : "+cell+"\n");
            write.print("   Gender        : "+gender+"\n");
            write.print("   Hobby         : "+hobby+"\n");
            write.print("   Subject       : "+subject+"\n");
            write.print("   About         : "+about);
            
            write.close();
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Write.class.getName()).log(Level.SEVERE, null, ex);
     
        }
        
    
    }//GEN-LAST:event_sSubmitMouseClicked

