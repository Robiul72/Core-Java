    public void employe() {

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(employe);
        txtTable.setModel(model);
        String sql = "select * from employe";

        try {
            ps = con.getCon().prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {

                int id = rs.getInt("employe_Id");
                String name = rs.getString("employe_Name");
                String designation = rs.getString("employe_designation");
                Float salary = rs.getFloat("employe_Salary");

                model.addRow(new Object[]{id, name, designation, salary});
            }

            ps.close();
            rs.close();
            con.getCon().close();

        } catch (SQLException ex) {
            Logger.getLogger(EmployeTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

