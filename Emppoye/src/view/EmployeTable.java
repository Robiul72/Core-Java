    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:

        sql = "update employe set employe_Name=?, employe_designation=?, employe_Salary=? where employe_Id=?";

        try {
            ps = con.getCon().prepareStatement(sql);

            ps.setString(1, txtName.getText());
            ps.setString(2, txtDesignation.getSelectedItem().toString());
            ps.setFloat(3, Float.parseFloat(txtSalary.getSelectedItem().toString()));
            ps.setInt(4, Integer.parseInt(txtId.getText().toString()));

            ps.executeUpdate();
            ps.close();
            con.getCon().close();

            JOptionPane.showMessageDialog(rootPane, "Data Updated");
            reset();
            employe();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Data not Updated");

            Logger.getLogger(EmployeTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

